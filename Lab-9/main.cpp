std::vector<int> find_boundary_vertices(const std::vector<std::vector<int>>& adjacency_list) {
    std::vector<int> boundary_vertices;

    for (int i = 0; i < adjacency_list.size(); ++i) {
        if (adjacency_list[i].size() <= 3) {
            boundary_vertices.push_back(i);
        }
    }

    return boundary_vertices;
}

void build_adjacency_list(TriangleMesh &mesh, std::vector<std::vector<int>> &adjacency_list) {
    for (const auto& tri : mesh.indices) {
        int vertices[3] = {tri.vtxi, tri.vtxj, tri.vtxk};
        for (int i = 0; i < 3; ++i) {
            int v1 = vertices[i];
            int v2 = vertices[(i + 1) % 3];
            adjacency_list[v1].push_back(v2);
            adjacency_list[v2].push_back(v1);
        }
    }
    for (auto& neighbors : adjacency_list) {
        std::sort(neighbors.begin(), neighbors.end());
        neighbors.erase(std::unique(neighbors.begin(), neighbors.end()), neighbors.end());
    }
}

std::vector<Vector> tutte_embedding(TriangleMesh &mesh, const std::vector<int> &boundary_vertices, std::vector<std::vector<int>> &adjacency_list, int nbr_iterations) {
    int n = boundary_vertices.size();
    std::vector<Vector> v(mesh.vertices.size());
    double s = 0.0;
    for (int i = 0; i < n; i++) {
        s += (mesh.vertices[boundary_vertices[(i + 1)%n]] - mesh.vertices[boundary_vertices[i]]).norm();
    }
    double cs = 0.0;
    for (int i = 0; i < n; i++) {
        double theta = 2*M_PI*cs/s;
        v[boundary_vertices[i]] = Vector(cos(theta), sin(theta), 0);
        cs += (mesh.vertices[boundary_vertices[(i + 1)%n]] - mesh.vertices[boundary_vertices[i]]).norm();
    }
    for (int iter = 0; iter < nbr_iterations; ++iter) {
        std::vector<Vector> new_positions = v;
        for (int i = 0; i < mesh.vertices.size(); i++) {
            if (std::find(boundary_vertices.begin(), boundary_vertices.end(), i) == boundary_vertices.end()) {
                Vector sum(0, 0, 0);
                int K = 0;
                for (int neighbor : adjacency_list[i]) {
                    sum = sum + v[neighbor];
                    K++;
                }
                if (K > 0) {
                    new_positions[i] = sum/K;
                }
            }
        }
        v = new_positions;
    }
    return v;
}

